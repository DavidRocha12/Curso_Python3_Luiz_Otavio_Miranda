"""
For in com listas
"""

lista = ["Maria", "Helena", "Luiz"]

for nomes in lista:
    print(nomes)

# Listas são elementos iteráveis do mesmo jeito que as strings
# mas no caso das strings a iteração vai ser entre as letras
# e as listas são iteráveis sobre os elementos dentro dela.

"""
Resultado:

Maria
Helena
Luiz
"""
